/**
 * 長い名前ほど先にマッチさせるためのソート済みキー配列。
 * 例: "新宿三丁目" を含むキャンペーン名で "新宿" より "新宿三丁目" を優先する。
 */
const SORTED_KEYS = Object.keys(AREA_TO_PREFECTURE).sort(
  (a, b) => b.length - a.length
);

export interface AreaMatchResult {
  prefecture: string;
  matchedKey: string;
}

/**
 * キャンペーン名から都道府県を判定する。
 *
 * 判定順:
 * 1. 【...】や［...］で囲まれたプレフィックスから探す
 * 2. キャンペーン名全体を対象に、長い地域名から順にマッチ
 *
 * マッチしない場合は null（未分類扱い）。
 */
export function matchAreaToPrefecture(
  campaignName: string | undefined | null
): AreaMatchResult | null {
  if (!campaignName) return null;
  const trimmed = campaignName.trim();

  const bracketMatch = trimmed.match(/^[\s]*[【［\[]\s*([^】］\]]+?)\s*[】］\]]/);
  if (bracketMatch) {
    const inside = bracketMatch[1];
    for (const key of SORTED_KEYS) {
      if (inside.includes(key)) {
        return { prefecture: AREA_TO_PREFECTURE[key], matchedKey: key };
      }
    }
  }

  for (const key of SORTED_KEYS) {
    if (trimmed.includes(key)) {
      return { prefecture: AREA_TO_PREFECTURE[key], matchedKey: key };
    }
  }
  return null;
}
